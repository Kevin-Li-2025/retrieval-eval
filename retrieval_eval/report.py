"""
Report generation: JSON (machine) + Markdown summary with failure cases.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Dict, Any, List


def write_json_report(result_dict: Dict[str, Any], path: str | Path) -> None:
    """Write full result (metrics + failure_cases + config) to JSON."""
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(result_dict, f, indent=2, ensure_ascii=False)


def write_markdown_report(
    result_dict: Dict[str, Any],
    path: str | Path,
    title: str = "Retrieval Eval Report",
) -> None:
    """Write human-readable Markdown: metrics table + failure cases for triage."""
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    metrics = result_dict.get("metrics") or {}
    failures = result_dict.get("failure_cases") or []
    config = result_dict.get("config") or {}

    lines = [
        f"# {title}",
        "",
        "## Metrics",
        "",
        "| Metric | Value |",
        "|--------|-------|",
    ]
    for k, v in metrics.items():
        if k == "num_queries":
            continue
        if isinstance(v, float):
            v = f"{v:.4f}" if 0 < v < 1 else str(v)
        lines.append(f"| {k} | {v} |")
    lines.append("")
    lines.append("## Failure cases (no relevant doc in top-1)")
    lines.append("")
    if not failures:
        lines.append("No failures.")
    else:
        lines.append(f"Total: {len(failures)}")
        lines.append("")
        for i, fc in enumerate(failures[:50], 1):  # cap at 50 in report
            qid = fc.get("query_id", "")
            query = (fc.get("query") or "")[:200]
            expected = fc.get("expected_ids", [])
            retrieved = fc.get("retrieved_top_k", [])[:5]
            lines.append(f"### {i}. `{qid}`")
            lines.append(f"- **Query:** {query}")
            lines.append(f"- **Expected doc ids:** {expected}")
            lines.append(f"- **Retrieved top-5:** {retrieved}")
            lines.append("")
        if len(failures) > 50:
            lines.append(f"... and {len(failures) - 50} more (see JSON).")
    lines.append("")
    lines.append("---")
    lines.append("*Generated by retrieval-eval*")

    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))


def load_result(path: str | Path) -> Dict[str, Any]:
    """Load a result JSON from a previous run."""
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)
